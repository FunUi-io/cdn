// custom css styling

// width
var width = this.style.getPropertyValue("--width");
this.style.setProperty("--width", parseInt(width));

var minWidth = this.style.getPropertyValue("--minWidth");
this.style.setProperty("--minWidth", parseInt(minWidth));

var maxWidth = this.style.getPropertyValue("--maxWidth");
this.style.setProperty("--maxWidth", parseInt(maxWidth));

// height
var height = this.style.getPropertyValue("--height");
this.style.setProperty("--height", parseInt(height));

var minHeight = this.style.getPropertyValue("--minHeight");
this.style.setProperty("--minHeight", parseInt(minHeight));

var maxHeight = this.style.getPropertyValue("--maxHeight");
this.style.setProperty("--maxHeight", parseInt(maxHeight));


// margin
var margin = this.style.getPropertyValue("--margin");
this.style.setProperty("--margin", parseInt(margin));

// padding
var padding = this.style.getPropertyValue("--padding");
this.style.setProperty("--padding", parseInt(padding));